## cacheSolve function checks if the inverse of a matrix created by above function exists . if exists it returns
## inverse matrix other wise calcuates it and stores it for next call to inverse matrix
cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'
  inv <- x$getInverse()
  if (!is.null(inv)) {
    message("getting cached data")
    return(inv)
  }
  mat <- x$get()
  inv <- solve(mat, ...)
  x$setInverse(inv)
  inv
}
